const llms = `# Monkey D Loopy

> A factory for bounded, crash-resumable agent loops. Define a LoopSpec, prove it is bounded and resume-stable, then compile it into a durable artifact with enforced time, token, and cost controls.

## Start here

- [Documentation home](${site}/): Product overview and quickstart.
- [First bounded loop](${site}/quickstart): Safe clean-room proof from npm to a journaled, portable artifact.
- [Using Loopy with agents](${site}/agent-guide): Recommended context and tool workflow for an agent.
- [Complete agent context](${site}/llms-full.txt): Consolidated canonical documentation in one text file.

## Documentation

${sources
  .filter((source) => source.file !== "README.md")
  .map((source) => `- [${source.title}](${source.url}): ${source.description}`)
  .join("\n")}

## Source and releases

- [GitHub repository](https://github.com/MaTriXy/Monkey.D.Loopy): Canonical source, examples, recipes, and issues.
- [v${version} release](https://github.com/MaTriXy/Monkey.D.Loopy/releases/tag/v${version}): Current documented release.
`;

const fullSections = await Promise.all(
  sources.map(async (source) => {
    const markdown = await readFile(path.join(root, source.file), "utf8");
    return `# ${source.title}\n\nCanonical page: ${source.url}\nSource: https://github.com/MaTriXy/Monkey.D.Loopy/blob/main/${source.file}\n\n${markdown.trim()}`;
  }),
);

const llmsFull = `# Monkey D Loopy — complete agent context

Generated from the canonical repository Markdown. Do not edit this file directly.
Documentation index: ${site}/llms.txt

${fullSections.join("\n\n---\n\n")}
`;

const outputs = [
  ["docs/public/llms.txt", llms],
  ["docs/public/llms-full.txt", llmsFull],
];

let stale = false;

for (const [relativePath, content] of outputs) {
  const outputPath = path.join(root, relativePath);
  if (check) {
    const current = await readFile(outputPath, "utf8").catch(() => "");
    if (current !== content) {
      console.error(`${relativePath} is stale; run pnpm agent-docs:generate`);
      stale = true;
    }
  } else {
    await writeFile(outputPath, content);
    console.log(`generated ${relativePath}`);
  }
}

if (stale) process.exitCode = 1;
