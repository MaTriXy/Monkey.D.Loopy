#!/usr/bin/env node
import { readFileSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const recipesDir = join(root, "recipes");
const output = join(root, "packages/core/src/recipe-catalog.generated.ts");

const sources = readdirSync(recipesDir, { withFileTypes: true })
  .filter((entry) => entry.isDirectory())
  .map((entry) => {
    const dir = join(recipesDir, entry.name);
    const manifest = readFileSync(join(dir, "recipe.json"), "utf8");
    const parsed = JSON.parse(manifest);
    const fixturePaths = Object.values(parsed.fixtures).sort();
    return {
      manifest,
      specYaml: readFileSync(join(dir, `${parsed.name}.loop.yaml`), "utf8"),
      readme: readFileSync(join(dir, "README.md"), "utf8"),
      fixtures: Object.fromEntries(fixturePaths.map((relativePath) => [relativePath, readFileSync(join(dir, relativePath), "utf8")])),
    };
  })
  .sort((a, b) => {
    const an = JSON.parse(a.manifest).name;
    const bn = JSON.parse(b.manifest).name;
    return an < bn ? -1 : an > bn ? 1 : 0;
  });

const generated = `/** GENERATED by scripts/generate-recipe-catalog.mjs. Do not edit by hand. */
import type { RecipeSource } from "./recipe.js";

export const BUILTIN_RECIPE_SOURCES: readonly RecipeSource[] = ${JSON.stringify(sources, null, 2)};
`;

if (process.argv.includes("--check")) {
  let current = "";
  try {
    current = readFileSync(output, "utf8");
  } catch {
    // The diagnostic below covers a missing generated file too.
  }
  if (current !== generated) {
    console.error("recipe catalog is stale; run `pnpm recipes:generate` and commit the result");
    process.exit(1);
  }
  console.log(`recipe catalog ✓ ${sources.length} recipes`);
} else {
  writeFileSync(output, generated);
  console.log(`generated ${sources.length} recipes → ${output}`);
}
