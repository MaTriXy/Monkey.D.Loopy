/**
 * Claude Code workflow-prose adapter. Lowers a LoopSpec to a markdown *execution guide*
 * (a skill/command) that a coding agent follows — the cc-wf-studio approach of compiling
 * flow semantics into prose + a self-documenting Mermaid diagram, rather than a runtime.
 * Enforcement is agent-followed (soft); the capability matrix flags this honestly.
 */
import type { Adapter, PlanResult, PlannedFile } from "./types.js";
import { capabilityWarnings } from "./types.js";
import type { LoopSpec, Step } from "../types.js";

function whenSuffix(step: Step): string {
  return step.when ? ` — only when \`${step.when}\`` : "";
}

function saveSuffix(save?: Record<string, string>): string {
  if (!save || Object.keys(save).length === 0) return "";
  const parts = Object.entries(save).map(([v, p]) => `\`${p}\` → \`state.${v}\``);
  return ` Save ${parts.join(", ")}.`;
}

function stepProse(step: Step, indent: string): string[] {
  const id = `**${step.id}**`;
  switch (step.kind) {
    case "agent":
      return [`${indent}- ${id} (agent via \`${step.harness}\`)${whenSuffix(step)}: ${step.prompt}${saveSuffix(step.save)}`];
    case "shell": {
      const cmd = step.args ? [step.cmd, ...step.args.map((a) => `'${a.replace(/'/g, `'\\''`)}'`)].join(" ") : step.cmd;
      return [`${indent}- ${id} run \`${cmd}\`${whenSuffix(step)}.${saveSuffix(step.save)}`];
    }
    case "http":
      return [`${indent}- ${id} HTTP ${step.request.method} \`${step.request.url}\`${whenSuffix(step)}.${saveSuffix(step.save)}`];
    case "breakpoint":
      return [`${indent}- ${id} ask the user: "${step.ask}"${whenSuffix(step)} — continue only if approved.`];
    case "sleep":
      return [`${indent}- ${id} ${step.for ? `wait ${step.for}` : `wait until \`${step.until}\``}${whenSuffix(step)}.`];
    case "reduce": {
      const head = `${indent}- ${id} for each \`${step.as ?? "item"}\` in \`${step.over}\`${whenSuffix(step)}:`;
      const body = step.body.flatMap((s) => stepProse(s, indent + "  "));
      return [head, ...body];
    }
  }
}

function mermaid(spec: LoopSpec): string {
  const cond = spec.terminate.until.replace(/[`"]/g, "").replace(/[{}]/g, "");
  const lines = ["flowchart TD", "  start([start]) --> i0"];
  spec.body.forEach((s, i) => {
    const label = s.id.replace(/[^A-Za-z0-9_.: -]/g, "");
    const next = i < spec.body.length - 1 ? `i${i + 1}` : "chk";
    lines.push(`  i${i}["${label}"] --> ${next}`);
  });
  lines.push(`  chk{"${cond}?"} -- no --> i0`);
  lines.push("  chk -- yes --> done([exit])");
  return lines.join("\n");
}

function emitGuide(spec: LoopSpec): string {
  const inputs = Object.entries(spec.inputs ?? {}).map(([n, d]) => `\`${n}\` (${d.type})`).join(", ") || "none";
  const state = Object.entries(spec.state?.vars ?? {}).map(([n, d]) => `\`${n}\` = ${JSON.stringify(d.init)}`).join(", ") || "none";
  const caps = spec.caps;
  const capLines = [`- Stop after at most **${caps.max_iterations}** iterations.`];
  if (caps.no_progress) capLines.push(`- If \`${caps.no_progress.fingerprint}\` repeats ${caps.no_progress.max_repeats}× unchanged, stop (no progress).`);
  if (caps.budget) capLines.push(`- Soft budget: ${JSON.stringify(caps.budget)} — stop if exceeded.`);
  capLines.push(`- On cap exceeded: ${caps.on_cap_exceeded ?? "breakpoint"}.`);
  const gateLines = (spec.gates ?? []).map(
    (g) => `- ${g.after ? `After \`${g.after}\`, ` : ""}${g.when ? `if \`${g.when}\`, ` : ""}ask: "${g.ask}" — continue only if approved.`
  );

  return `---
name: ${spec.id}
description: ${spec.meta?.description ?? `Run the ${spec.id} loop`}
---

# ${spec.meta?.name ?? spec.id} — loop execution guide

> Generated by **Monkey D Loopy** (claude-code prose target). No runtime ships for this
> target — **you are the executor**. Follow the body each iteration; the caps below are
> limits you must honor yourself (this target enforces nothing automatically).

- **Pattern:** ${spec.pattern}
- **Exit when:** \`${spec.terminate.until}\` (signal: ${spec.terminate.signal})
- **Inputs:** ${inputs}
- **State (initial):** ${state}

## Each iteration — repeat the body until the exit condition holds
${spec.body.flatMap((s) => stepProse(s, "")).join("\n")}

After the body, re-check the exit condition: \`${spec.terminate.until}\`. If it holds, stop${spec.terminate.on_exit ? ` and run the on-exit action (${spec.terminate.on_exit.kind})` : ""}.

## Termination & caps (honor these)
${capLines.join("\n")}
${gateLines.length ? `\n## Human gates\n${gateLines.join("\n")}` : ""}

## Flow
\`\`\`mermaid
${mermaid(spec)}
\`\`\`
`;
}

export const claudeCodeAdapter: Adapter = {
  target: "claude-code",
  plan(spec: LoopSpec): PlanResult {
    const files: PlannedFile[] = [
      { relativePath: `${spec.id}.loop.md`, contents: emitGuide(spec), kind: "skill" },
      {
        relativePath: "loop.lock",
        contents:
          JSON.stringify(
            { loop_id: spec.id, loopspec_version: spec.loopspec, target: "claude-code", signal: spec.terminate.signal, caps: spec.caps },
            null,
            2
          ) + "\n",
        kind: "provenance",
      },
    ];
    return { target: "claude-code", files, warnings: capabilityWarnings(spec, "claude-code") };
  },
};
